#!/usr/bin/env node
// conscientious — SessionStart hook that:
//   1. Installs/refreshes ~/.claude/conscientious-statusline.{sh,ps1} — a small
//      launcher that resolves the current plugin install at exec time. The
//      launcher's path is stable across plugin updates, so users only have to
//      configure ~/.claude/settings.json once.
//   2. Emits a setup or migration nudge depending on what's currently in
//      settings.json:
//        - no statusLine                              -> "STATUSLINE SETUP NEEDED"
//        - command references a /plugins/cache/ path  -> "STATUSLINE PATH NEEDS MIGRATION"
//        - command already references the launcher    -> silent
//        - any other command (user override)          -> silent

const fs = require('fs');
const path = require('path');
const os = require('os');

const claudeDir = process.env.CLAUDE_CONFIG_DIR || path.join(os.homedir(), '.claude');
const settingsPath = path.join(claudeDir, 'settings.json');
const isWindows = process.platform === 'win32';
const launcherName = isWindows ? 'conscientious-statusline.ps1' : 'conscientious-statusline.sh';
const launcherPath = path.join(claudeDir, launcherName);

// Resolve marketplace + plugin name by walking up from __dirname. Layout:
//   <claudeDir>/plugins/cache/<marketplace>/<plugin>/<HASH>/hooks/<this file>
// We validate the "cache" sentinel so --plugin-dir / zip installs (which don't
// have a marketplaces clone or a hash dir) fall through to the legacy behaviour.
function resolvePluginNames() {
    const hooksDir = __dirname;
    const hashDir = path.dirname(hooksDir);
    const pluginDir = path.dirname(hashDir);
    const marketplaceDir = path.dirname(pluginDir);
    const cacheDir = path.dirname(marketplaceDir);
    if (path.basename(cacheDir) !== 'cache') return null;
    return {
        marketplace: path.basename(marketplaceDir),
        plugin: path.basename(pluginDir),
    };
}

function generateLauncherBody({ marketplace, plugin }) {
    if (isWindows) {
        return [
            '# Auto-generated by conscientious-statusline-nudge.js — do not edit by hand.',
            '# Resolves the current `conscientious` plugin install and execs its statusline',
            '# script. Stable across plugin updates so settings.json never needs to change.',
            '$ClaudeDir = if ($env:CLAUDE_CONFIG_DIR) { $env:CLAUDE_CONFIG_DIR } else { Join-Path $HOME ".claude" }',
            '$MP  = ' + JSON.stringify(marketplace),
            '$PLG = ' + JSON.stringify(plugin),
            '',
            '# Preferred: the marketplace\'s local git clone, which Claude Code maintains',
            '# in-place via `git pull`.',
            '$Candidate = Join-Path $ClaudeDir ("plugins/marketplaces/" + $MP + "/plugins/" + $PLG + "/hooks/conscientious-statusline.ps1")',
            'if (Test-Path -LiteralPath $Candidate) {',
            '    & $Candidate',
            '    exit $LASTEXITCODE',
            '}',
            '',
            '# Fallback: newest versioned cache install (by mtime). Lexicographic',
            '# sort would be wrong because cache dir names are commit SHAs.',
            '$CacheParent = Join-Path $ClaudeDir ("plugins/cache/" + $MP + "/" + $PLG)',
            'if (Test-Path -LiteralPath $CacheParent) {',
            '    $Latest = Get-ChildItem -LiteralPath $CacheParent -Directory -ErrorAction SilentlyContinue |',
            '        Sort-Object LastWriteTime -Descending |',
            '        ForEach-Object { Join-Path $_.FullName "hooks/conscientious-statusline.ps1" } |',
            '        Where-Object { Test-Path -LiteralPath $_ } |',
            '        Select-Object -First 1',
            '    if ($Latest) {',
            '        & $Latest',
            '        exit $LASTEXITCODE',
            '    }',
            '}',
            '',
            '# Plugin not installed; print a one-line hint instead of breaking the prompt.',
            '[Console]::Write("conscientious: statusline script not found (plugin missing?)")',
            'exit 0',
            '',
        ].join('\r\n');
    }
    return [
        '#!/bin/bash',
        '# Auto-generated by conscientious-statusline-nudge.js — do not edit by hand.',
        '# Resolves the current `conscientious` plugin install and execs its statusline',
        '# script. Stable across plugin updates so ~/.claude/settings.json never needs',
        '# to change.',
        'CLAUDE_DIR="${CLAUDE_CONFIG_DIR:-$HOME/.claude}"',
        'MP=' + JSON.stringify(marketplace),
        'PLG=' + JSON.stringify(plugin),
        '',
        '# Preferred: the marketplace\'s local git clone, which Claude Code maintains',
        '# in-place via `git pull`.',
        'CANDIDATE="$CLAUDE_DIR/plugins/marketplaces/$MP/plugins/$PLG/hooks/conscientious-statusline.sh"',
        'if [ -r "$CANDIDATE" ]; then',
        '    exec bash "$CANDIDATE"',
        'fi',
        '',
        '# Fallback: newest versioned cache install (by mtime). Lexicographic sort',
        '# would be wrong because cache dir names are commit SHAs, not timestamps.',
        'LATEST=$(ls -t "$CLAUDE_DIR/plugins/cache/$MP/$PLG/"*/hooks/conscientious-statusline.sh 2>/dev/null | head -n 1)',
        'if [ -n "$LATEST" ] && [ -r "$LATEST" ]; then',
        '    exec bash "$LATEST"',
        'fi',
        '',
        '# Plugin not installed; print a one-line hint instead of breaking the prompt.',
        'printf \'conscientious: statusline script not found (plugin missing?)\'',
        'exit 0',
        '',
    ].join('\n');
}

function installLauncher(body) {
    let current = null;
    try { current = fs.readFileSync(launcherPath, 'utf8'); } catch {}
    if (current === body) return;
    const tmp = launcherPath + '.tmp-' + process.pid;
    fs.writeFileSync(tmp, body, { mode: 0o755 });
    fs.renameSync(tmp, launcherPath);
}

function readSettings() {
    try {
        const raw = fs.readFileSync(settingsPath, 'utf8');
        return JSON.parse(
            raw
                .replace(/\/\*[\s\S]*?\*\//g, '')
                .replace(/\/\/[^\n]*/g, '')
        );
    } catch {
        return null;
    }
}

function buildSnippet(suggestedCommand) {
    return '"statusLine": { "type": "command", "command": '
        + JSON.stringify(suggestedCommand)
        + ', "refreshInterval": 1 }';
}

function emitSetup(snippet) {
    process.stdout.write(
        'STATUSLINE SETUP NEEDED: The conscientious plugin includes a combined statusline badge ' +
        '(e.g. "Clarify: ON | Biblio: AUTO | Reminders: 3 (Propose: ON)") covering /clarify, /biblio, and /remind-me. ' +
        'It is not configured yet. To enable, add this to ' + settingsPath + ': ' + snippet + ' ' +
        'Proactively offer to set this up for the user on first interaction.'
    );
}

function emitMigration(currentCommand, snippet) {
    process.stdout.write(
        'STATUSLINE PATH NEEDS MIGRATION: ' + settingsPath + ' currently configures statusLine.command as ' +
        JSON.stringify(currentCommand) + ', which references a per-version plugin cache directory ' +
        '(.../plugins/cache/<marketplace>/<plugin>/<HASH>/...). That path goes stale on every conscientious plugin update. ' +
        'A stable launcher has been installed at ' + launcherPath + ' that resolves the current install at exec time. ' +
        'Proactively offer to update settings.json statusLine to: ' + snippet
    );
}

function main() {
    const names = resolvePluginNames();
    let launcherInstalled = false;
    if (names) {
        try {
            installLauncher(generateLauncherBody(names));
            launcherInstalled = fs.existsSync(launcherPath);
        } catch {
            // Silent — fall through to a sensible fallback command below.
        }
    }

    // With the launcher on disk we recommend its stable path. Otherwise (e.g.
    // --plugin-dir dev install) fall back to the legacy __dirname path so
    // plugin authors testing locally still get a working statusline.
    const realScriptName = isWindows ? 'conscientious-statusline.ps1' : 'conscientious-statusline.sh';
    const suggestedCommand = launcherInstalled
        ? (isWindows
            ? 'powershell -ExecutionPolicy Bypass -File "' + launcherPath + '"'
            : 'bash "' + launcherPath + '"')
        : (isWindows
            ? 'powershell -ExecutionPolicy Bypass -File "' + path.join(__dirname, realScriptName) + '"'
            : 'bash "' + path.join(__dirname, realScriptName) + '"');
    const snippet = buildSnippet(suggestedCommand);

    const settings = readSettings();
    const currentCommand = settings && settings.statusLine && typeof settings.statusLine.command === 'string'
        ? settings.statusLine.command
        : null;

    if (!currentCommand) {
        emitSetup(snippet);
        return;
    }

    if (currentCommand.includes(launcherPath)) {
        // Already on the stable path; nothing to do.
        return;
    }

    const cacheFragment = names
        ? '/plugins/cache/' + names.marketplace + '/' + names.plugin + '/'
        : '/plugins/cache/';
    if (currentCommand.includes(cacheFragment)) {
        emitMigration(currentCommand, snippet);
        return;
    }

    // Anything else is a deliberate user override; stay silent.
}

try { main(); } catch {
    // Silent fail — never block session start over statusline detection.
}
